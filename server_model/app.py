# Image preprocessing function (adjust based on model input)
def preprocess_image(image_bytes):
    image = Image.open(io.BytesIO(image_bytes)).convert("RGB")
    image = image.resize((224, 224))  # adjust size as per your model
    img_array = np.array(image)
    return np.expand_dims(img_array, axis=0)

@app.post("/predict")
async def predict(file: UploadFile = File(...)):
    contents = await file.read()
    input_tensor = preprocess_image(contents)
    prediction = model.predict(input_tensor)
    # Convert prediction to string (adjust based on your model output)
    # print(prediction)
    predicted_class = np.argmax(prediction)
    return {"result": class_names[predicted_class]}


